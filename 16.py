
# sep, what is sep how to use sep
# sep is to separate the arguments by a colon

print ('The value of 3+4 is', 3+4, '.')
print ('The value of 3+4 is ', 3+4, '.', sep='')

# end, what is end how to use end
# There is an optional argument called end that you can use
# to keep the print function from advancing to the next line

print('On the first line', end='  ')
print('On the second line')

